/**
 * This class provides some utility methods.
 * 
 * @author manuel
 *
 */
public class Util {

  /**
   * Converts a boolean value to a character. The character 'Y' represents true,
   * while the character 'N' represents false.
   * 
   * @param B
   *          the boolean value
   * @return 'Y' if B is true, or 'N' if it is false.
   */
  public static char convertBool(boolean B) {
    return (B ? 'Y' : 'N');
  }

}
